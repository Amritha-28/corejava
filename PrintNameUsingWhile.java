//Example prgm for while loop
package whileLoopprograms;

public class PrintNameUsingWhile {

	public static void main(String[] args) {
		int i;
		i=1;                                   //initialization
		while(i<=7)                           //condition to check
		{
			System.out.println("Amritha");   //body
			i++;                             //increment
		}

	}

}
